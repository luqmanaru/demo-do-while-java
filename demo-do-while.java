/**
 * Demonstrasi Do-While Loop
 * @author luqmanaru
 */
public class PerulanganDoWhile {
    public static void main(String[] args) { 
        int x = 1; // Inisialisasi counter
        
        // Do-while loop
        do {
            System.out.println(x); 
            x++; // Increment counter
        } while (x < 10);
    }
}
